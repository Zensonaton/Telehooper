		await msg.answer("<b>–ß—Ç–æ-—Ç–æ –ø–æ—à–ª–æ –Ω–µ —Ç–∞–∫ üòÖ</b>\n\n–¢—ã –Ω–µ –æ—á–µ–Ω—å –ø–æ—Ö–æ–∂ –Ω–∞ –ü–∞–≤–ª–∞ –î—É—Ä–æ–≤–∞.")

